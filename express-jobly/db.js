/** Database setup for jobly. */
import pkg from 'pg';
import dotenv from 'dotenv';
import { getDatabaseUri } from './config.js';

dotenv.config();
const { Client } = pkg;

const db = new Client({
    user: process.env.DB_USER || 'your_username', // Replace 'your_username' with your PostgreSQL username
    database: getDatabaseUri(), // Use the database URI generated by the function
    password: process.env.DB_PASSWORD || 'your_password', // Replace 'your_password' with your PostgreSQL password
});

db.connect()
    .then(() => console.log('Connected to the database'))
    .catch((error) => console.error('Error connecting to the database:', error));

export default db;
